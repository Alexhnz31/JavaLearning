package lesson4;

public class RentalPoint {
    private String id;
    private int houseId;
    private String nameOfTown;
    private String nameOfStreet;
    private String nameOfCounty;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getHouseId() {
        return houseId;
    }

    public void setHouseId(int houseId) {
        this.houseId = houseId;
    }

    public String getNameOfTown() {
        return nameOfTown;
    }

    public void setNameOfTown(String nameOfTown) {
        this.nameOfTown = nameOfTown;
    }

    public String getNameOfStreet() {
        return nameOfStreet;
    }

    public void setNameOfStreet(String nameOfStreet) {
        this.nameOfStreet = nameOfStreet;
    }

    public String getNameOfCounty() {
        return nameOfCounty;
    }

    public void setNameOfCounty(String nameOfCounty) {
        this.nameOfCounty = nameOfCounty;
    }
}
